data=["pisang","mangga","jeruk","leci","anggur"]

print(data)
print("")
print("data [0:2] --> dari awal hingga sebelum indeks 2 :",data[0:2])
print("data [:3] --> 3 data pertama :",data[:3])
print("data [-3:] --> 3 data terakhir :",data[-3:])
print("data [::2] --> lompat 2, mulai elemen pertama :",data[::2])
print("data [3::2] --> lompat 2 muali indeks ke 3 :",data[3::2])
print("data [-2::1] --> lompat satu ke kanan, muali indeks ke 2 :",data[-2::1])
print("data [-2::-1] --> lompat 1 ke kiri, muali indeks ke 2 :",data[-1::-2])